<?php
  //Operadores Matematicos
  $a = 10;
  $b = 55;
  $c = 3.14;
  $d = 66;

  //Adicao
  $soma = $a + $b + $d;
  echo "Soma é: $soma <br>";

  //Subtracao
  $sub = $a - $d;
  echo "Subtração é: $sub <br>";

  //Divisao
  $div = $c / $a;
  echo "Divisão é: $div <br>";

  //Multiplicacao
  $mult = $a * $c;
  echo "Multiplicação é: $mult <br>";

  //Resto da divisao
  $resto = $d % 2;
  echo "Resto da divisão é: ".$resto;
?>